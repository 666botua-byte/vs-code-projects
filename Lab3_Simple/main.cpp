#include <iostream>

#include "classes.h"

// g++ -std=c++11 classes.cpp main.cpp -o lab3

int main() {
    // ── КРОК 1: Створення об'єктів ──
    Region r1("Львівська область", "Україна", 21833.0, 2522000);
    City c1("Львів", "Україна", 182.0, 728000, "Садовий А.", 1256);
    Megalopolis m1("Київ", "Україна", 839.0, 3703100, "Кличко В.", 482, 10, "Столиця");

    // ── КРОК 2: Спосіб 1 — ручне додавання через вказівник ──
    Place* ptr;
    ptr = &r1; ptr->add();
    ptr = &c1; ptr->add();
    ptr = &m1; ptr->add();
    std::cout << "\n=== Колекція після РУЧНОГО додавання ===" << std::endl;
    Place::printAll();

    // ── КРОК 3: Спосіб 2 — автоматичне додавання в конструкторі ──
    // У конструкторі Place() є: collection[count] = this;
    // Тому нові об'єкти додаються автоматично під час створення.
    Region r2("Одеська область", "Україна", 33310.0, 2380000);
    City c2("Одеса", "Україна", 162.0, 993120, "Труханов Г.", 1794);
    std::cout << "\n=== Колекція після АВТОМАТИЧНОГО додавання ===" << std::endl;
    Place::printAll();

    // ── КРОК 4: Віртуальні vs НЕ віртуальні методи ──
    Place* p = &m1;
    std::cout << "\n=== Виклик ВІРТУАЛЬНОГО show() через Place* ===" << std::endl;
    p->show();

    std::cout << "\n=== Виклик НЕ ВІРТУАЛЬНОГО info() через Place* ===" << std::endl;
    p->info();
    // Навіть якщо p вказує на Megalopolis, невіртуальний метод
    // викликає версію типу вказівника (Place), а не реального об'єкта.

    // ── КРОК 5: Статичний метод через клас ──
    std::cout << "\n=== Фінальний перегляд колекції ===" << std::endl;
    Place::printAll();

    // ── КРОК 6: Лічильник об'єктів ──
    std::cout << "\nЗагальна кількість створених об'єктів: " << Place::count + 1 << std::endl;

    return 0;
}

